public class swap {
    public static void main(String[] args) {
        
        int a =5;
        int b =6;
        System.out.println("before swaping a ="+a);
        System.out.println("before swaping b ="+b);
         
        int temp =a;
        a=b;
        b= temp;
        
System.out.println("after swaping a ="+a);
System.out.println("after swaping b =" +b);

    }
}
